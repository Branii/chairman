    public static function getTimeSet(string $lotteryId)
    {
        $data = [
            implode(",", self::game1x0()) => self::time1x0(),
            implode(",", self::game1x5()) => self::time1x5(),
            implode(",", self::game3x0()) => self::time3x0(),
            implode(",", self::game5x0()) => self::time5x0()
        ];
        foreach ($data as $keys => $time) {
            $keysArray = explode(',', $keys);
            if (in_array($lotteryId, $keysArray)) {
                return $time;
            }
        }
        return null;

    }

